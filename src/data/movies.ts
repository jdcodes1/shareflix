export const getMovieById = (id: number): Movie | undefined => {
  return movies.find(movie => movie.id === id);
};

export const getMoviesByIds = (ids: number[]): Movie[] => {
  return ids.map(id => getMovieById(id)).filter((movie): movie is Movie => movie !== undefined);
};

export const searchMovies = (query: string): Movie[] => {
  const lowerQuery = query.toLowerCase();
  return movies.filter(movie =>
    movie.title.toLowerCase().includes(lowerQuery) ||
    movie.genres.some(genre => genre.toLowerCase().includes(lowerQuery)) ||
    movie.overview.toLowerCase().includes(lowerQuery)
  );
};

export const getMoviesByGenre = (genre: string): Movie[] => {
  return movies.filter(movie =>
    movie.genres.some(g => g.toLowerCase() === genre.toLowerCase())
  );
};

export const getAllGenres = (): string[] => {
  const genres = new Set<string>();
  movies.forEach(movie => movie.genres.forEach(genre => genres.add(genre)));
  return Array.from(genres).sort();
};
